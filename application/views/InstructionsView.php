            <h1>What is BYOBracket?</h1>
            <p>With March Madness rapidly approaching, we wanted to put together a simple little web application that will help all of the college basketball fans out there have a useful tool to predict how the bracket seeds will be filled and ultimately play out.  Of course, what use would that be if you couldn't also save and share the brackets with your friends?</p>
            <br />
            <p>Three Easy Steps:</p>
            <ul>
            <li>1. Click a seed and pick a team, repeat x 68.</li>
            <li>2. Tweak your seeding by dragging and swapping seeds.</li>
            <li>3. Click &quot;Share Your Bracket&quot; and fire off a tweet or copy and paste a link to your friend.</li>
            </ul>
            <br />
            <p><b><i>Alert: Your bracket will only be saved for future editing if you are logged in!</i></b></p>
        </div>
        <div id="instructions2" class="instructionsQuestion">
            <h1>Do I have to register to start a bracket?</h1>
            <p>No, but keep in mind you'll only be able to come back and make edits to your saved bracket if you are logged in.</p>
        </div>
        <div id="instructions3" class="instructionsQuestion">
            <h1>How do I start making a bracket?</h1>
            <p>Choose bracket mode or seed mode to view all of the available slots as it's most convenient for you. Then just click on a seed, start typing in your team names and make your picks. Once the teams show up in your bracket you can move them around as you please by dragging or clicking the X to remove them.</p>
            <p>Keep in mind there is no editing from linked brackets!</p>
        </div>
        <div id="instructions4" class="instructionsQuestion">
            <h1>How can I share a bracket?</h1>
            <p>Share a bracket you just started or have completed, it doesn't matter if you've picked 1 or 68 teams. Just click the &quot;Share Bracket&quot; button from the top navigation and choose your method of sharing!</p>
        </div>
